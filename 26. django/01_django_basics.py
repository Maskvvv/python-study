# ============================================
# 1. Django 是什么？
# ============================================
"""
Django 是一个高级的 Python Web 框架，它鼓励快速开发和简洁实用的设计。
由经验丰富的开发者构建，它解决了 Web 开发中的许多麻烦，
让你可以专注于编写应用程序，而无需重新发明轮子。

核心特点：
- MTV 架构模式（Model-Template-View）
- 强大的 ORM（对象关系映射）
- 自动化的管理后台
- 优雅的 URL 设计
- 内置的安全机制
"""

# ============================================
# 2. MTV 架构模式
# ============================================
"""
Django 使用 MTV（Model-Template-View）架构：

┌─────────────────────────────────────────┐
│              用户请求                      │
└──────────────┬──────────────────────────┘
               │
               ▼
┌─────────────────────────────────────────┐
│          URL 分发器 (urls.py)            │
│     将 URL 映射到对应的视图函数             │
└──────────────┬──────────────────────────┘
               │
               ▼
┌─────────────────────────────────────────┐
│           视图层 (views.py)              │
│     处理业务逻辑，调用模型获取数据           │
└──────────┬───────────────┬──────────────┘
           │               │
           ▼               ▼
┌──────────────────┐  ┌──────────────────┐
│  模型层          │  │  模板层          │
│  (models.py)     │  │  (templates/)    │
│  数据库操作       │  │  HTML 渲染       │
└──────────────────┘  └──────────────────┘

Model（模型）：
    - 负责数据存取层
    - 与数据库交互
    - 定义数据结构

Template（模板）：
    - 负责表现层
    - HTML 页面展示
    - 动态渲染数据

View（视图）：
    - 负责业务逻辑
    - 处理用户请求
    - 返回响应
"""

# ============================================
# 3. Django 项目结构
# ============================================
"""
一个典型的 Django 项目结构：

myproject/                    # 项目根目录
├── manage.py                 # Django 管理脚本
├── myproject/                # 项目配置目录
│   ├── __init__.py
│   ├── settings.py           # 项目设置
│   ├── urls.py               # 主 URL 配置
│   ├── asgi.py               # ASGI 服务器配置
│   └── wsgi.py               # WSGI 服务器配置
└── myapp/                    # 应用目录
    ├── __init__.py
    ├── admin.py              # 管理后台配置
    ├── apps.py               # 应用配置
    ├── models.py             # 数据模型
    ├── views.py              # 视图函数
    ├── urls.py               # 应用 URL 配置
    ├── tests.py              # 测试文件
    └── migrations/           # 数据库迁移文件
        └── __init__.py
"""

# ============================================
# 4. Django 的核心组件
# ============================================
"""
1. ORM（对象关系映射）
   - 用 Python 类定义数据库表
   - 无需编写 SQL 语句
   - 支持多种数据库

2. 路由系统
   - URL 到视图的映射
   - 支持正则表达式
   - 支持命名路由

3. 模板引擎
   - 动态 HTML 生成
   - 模板继承
   - 自定义标签和过滤器

4. 表单处理
   - 表单验证
   - CSRF 保护
   - 模型表单

5. 管理后台
   - 自动生成后台界面
   - 可自定义
   - 权限管理

6. 缓存系统
   - 多种缓存后端
   - 粒度控制
   - 性能优化

7. 安全机制
   - CSRF 保护
   - XSS 防护
   - SQL 注入防护
   - 密码哈希
"""

# ============================================
# 5. Django vs Flask
# ============================================
"""
Django：
    ✅ 全栈框架，功能齐全
    ✅ 内置 ORM、Admin、认证系统
    ✅ 适合大型项目
    ✅ 约定优于配置
    
Flask：
    ✅ 微框架，轻量灵活
    ✅ 需要自己选择扩展
    ✅ 适合小型项目
    ✅ 配置优于约定

选择建议：
    - 如果你需要快速开发完整应用 → Django
    - 如果你只需要简单的 API 服务 → Flask
    - 如果你是初学者 → Django（文档完善）
"""

# ============================================
# 6. 安装 Django
# ============================================
"""
# 安装 Django
pip install django

# 查看版本
python -m django --version

# 创建项目
django-admin startproject myproject

# 创建应用
cd myproject
python manage.py startapp myapp

# 运行开发服务器
python manage.py runserver

# 创建数据库迁移
python manage.py makemigrations

# 执行迁移
python manage.py migrate

# 创建超级用户
python manage.py createsuperuser
"""

